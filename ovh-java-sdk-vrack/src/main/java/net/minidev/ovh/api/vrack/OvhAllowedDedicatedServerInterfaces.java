package net.minidev.ovh.api.vrack;

/**
 * A structure giving all dedicated server interfaces allowed for this vrack
 */
public class OvhAllowedDedicatedServerInterfaces {
	/**
	 * the name of dedicatedServer
	 *
	 * canBeNull
	 */
	public String dedicatedServer;

	/**
	 * the name of dedicatedServerInterface
	 *
	 * canBeNull
	 */
	public String name;

	/**
	 * the unique identifier of dedicatedServerInterface
	 *
	 * canBeNull
	 */
	public String dedicatedServerInterface;
}
