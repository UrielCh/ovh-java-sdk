package net.minidev.ovh.api.xdsl.eligibility;

/**
 * Represents a city
 */
public class OvhCity {
	/**
	 * The zip code of the city
	 *
	 * canBeNull
	 */
	public String zipCode;

	/**
	 * The identifier of the city
	 *
	 * canBeNull
	 */
	public String inseeCode;

	/**
	 * The name of the city
	 *
	 * canBeNull
	 */
	public String name;
}
