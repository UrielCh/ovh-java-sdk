package net.minidev.ovh.api.xdsl.linediagnostic;

/**
 * possible value for specific answer
 */
public class OvhPossibleValue {
	/**
	 * answer choice string
	 *
	 * canBeNull
	 */
	public String label;

	/**
	 * answer id
	 *
	 * canBeNull
	 */
	public Long id;

	/**
	 * answer choice value
	 *
	 * canBeNull
	 */
	public String value;
}
