package net.minidev.ovh.api.telephony;

/**
 * Represents a city
 */
public class OvhCity {
	/**
	 * The zip code of the city
	 *
	 * canBeNull
	 */
	public String zipCode;

	/**
	 * The name of the city
	 *
	 * canBeNull
	 */
	public String name;

	/**
	 * The identifier of the city
	 *
	 * canBeNull
	 */
	public String administrationCode;
}
